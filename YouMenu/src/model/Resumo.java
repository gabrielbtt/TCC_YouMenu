package model;

/**
 *
 * @author Matheus
 */
public class Resumo {
    int idProduto;
    String nome;
    float preco;
    
    public Resumo(){
        
    }

    public Resumo(int idProduto, String nome, float preco) {
        this.idProduto = idProduto;
        this.nome = nome;
        this.preco = preco;
    }

    public int getIdProduto() {
        return idProduto;
    }

    public void setIdProduto(int idProduto) {
        this.idProduto = idProduto;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getPreco() {
        return preco;
    }

    public void setPreco(float preco) {
        this.preco = preco;
    }
    
    
}
